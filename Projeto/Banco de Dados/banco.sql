CREATE TABLE tbcliente
( ID INT NOT NULL AUTO_INCREMENT,
CPF VARCHAR (11) ,
NOME VARCHAR (100) ,
ENDERECO1 VARCHAR (150) ,
ENDERECO2 VARCHAR (150) ,
BAIRRO VARCHAR (50) ,
CIDADE VARCHAR (50) ,
ESTADO VARCHAR (2) ,
CEP VARCHAR (8) ,
IDADE SMALLINT,
SEXO VARCHAR (1) ,
TELEFONE VARCHAR(12) ,
EMAIL VARCHAR(50) ,
PRIMARY KEY (ID) 
);

CREATE TABLE tbaluno
(  ID INT NOT NULL AUTO_INCREMENT,
NOME VARCHAR (100) ,
ENDERECO1 VARCHAR (150) ,
ENDERECO2 VARCHAR (150) ,
BAIRRO VARCHAR (50) ,
CIDADE VARCHAR (50) ,
ESTADO VARCHAR (2) ,
CEP VARCHAR (8) ,
DATANASCIMENTO DATE ,
IDADE SMALLINT,
SEXO VARCHAR (1) ,
ESCOLA VARCHAR(12) ,
SERIE VARCHAR(50) ,
TURMA VARCHAR(2) ,
PROFESSOR VARCHAR (50) ,
PRIMARY KEY (ID) 
);

CREATE TABLE tbFILIACAO
( IDPAI INT NOT NULL, 
IDFILHO INT NOT NULL, 
PRIMARY KEY (IDPAI, IDFILHO),  
FOREIGN KEY (IDPAI)
REFERENCES tbcliente (ID)
ON UPDATE RESTRICT ON DELETE CASCADE,
FOREIGN KEY (IDFILHO)
REFERENCES tbaluno(Id)
ON UPDATE RESTRICT ON DELETE CASCADE
);

CREATE TABLE tbPAGAMENTO
(  ID INT NOT NULL AUTO_INCREMENT ,
IDCLIENTE INT NOT NULL ,
DATAINICIO DATE ,
DATAPAGAMENTO DATE,
VALOR FLOAT,
FORMAPAGAMENTO VARCHARACTER (10),
PRIMARY KEY (ID) ,
FOREIGN KEY (IDCLIENTE)
REFERENCES tbcliente(Id)
ON UPDATE RESTRICT ON DELETE CASCADE
);

CREATE TABLE `tb_comments` (
